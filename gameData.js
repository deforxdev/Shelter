        const professionExp = Math.floor(Math.random() * (age - 18 + 1));

        // Хобі та стаж
        const hobby = this.getRandomItem(this.hobbies);
        const hobbyExp = Math.floor(Math.random() * (age - 16 + 1));

        // Здоров'я (40% ідеальне здоров'я)
        const health = Math.random() < 0.4 ? "Ідеальне здоров'я" : this.getRandomItem(this.healthList);

        // Фобія (40% без фобій)
